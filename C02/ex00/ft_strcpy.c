char	*ft_strcpy(char *dest, char *src)
{
	int count;
	int i;

	count = 0;
	i = -1;
	while (src[++i] != '\0')
	{
		count++;
	}
	i = -1;
	while (i++ < count)
	{
		dest[i] = src[i];
	}
	return (&dest[0]);
}
