#include "ft_lib.h"

int			ft_atoi(char *str)
{
	int	num;
	int i;

	num = 0;
	i = -1;
	while (str[++i])
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (-1);
	while (*str >= '0' && *str <= '9')
	{
		num = 10 * num + (*str - '0');
		str++;
	}
	return (num);
}
