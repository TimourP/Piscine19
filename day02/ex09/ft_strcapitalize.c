char	*ft_strcapitalize(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
	{
		if (97 <= str[i] && str[i] <= 122)
		{
			if (i > 0)
			{
				if (65 <= str[i - 1] && str[i - 1] <= 90)
				{
					;
				}
				else if (97 <= str[i - 1] && str[i - 1] <= 122)
				{
					;
				}
				else
					str[i] -= 32;
			}
			else
				str[i] -= 32;
		}
	}
	return (str);
}
