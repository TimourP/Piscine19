int		ft_str_is_alpha(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
	{
		if (65 <= str[i] && str[i] <= 90)
		{
			;
		}
		else if (97 <= str[i] && str[i] <= 122)
		{
			;
		}
		else
			return (0);
	}
	return (1);
}
