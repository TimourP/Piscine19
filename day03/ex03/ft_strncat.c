char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int i;

	i = -1;
	while (src[++i] != '\0' && i < nb)
	{
		dest[i] = src[i];
	}
	dest[i] = '\0';
	return (dest);
}
