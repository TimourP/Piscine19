int			ft_atoi(char *str)
{
	int	num;
	int	neg;

	neg = 1;
	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg = !neg;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = 10 * num + ('0' - *str);
		str++;
	}
	if (neg)
		num = -num;
	return (num);
}
